class Producto{
    constructor(ID, nombre, Categoria, Descripcion, Direccion, Marca, envio, Stock, Precio, TipoPago, Estado, Color){
        this.ID = ID;
        this.NombreProducto = nombre;
        this.Categoria = Categoria
        this.Descripcion = Descripcion
        this.Direccion = Direccion
        this.Marca = Marca
        this.envio = envio
        this.Stock = Stock
        this.Precio = Precio
        this.Pago = TipoPago
        this.Estado = Estado
        this.Color = Color
    }
}